package BasicJava;

public class ifloop {
	public static void main(String[] args) {
		String name = "maitreyee kane";
		System.out.println(name.length());
		System.out.println(name.charAt(5));
		System.out.println(name.lastIndexOf("e"));
		System.out.println(name.indexOf("y"));
		System.out.println(name.replace("a", "1"));
		System.out.println(name.replaceAll("ee", "i"));
		System.out.println(name.toLowerCase());
		System.out.println(name.toUpperCase());
		System.out.println(name.startsWith("maitreyee"));
		System.out.println(name.endsWith("Kane"));
		System.out.println(name.substring(6));

	}

}
